#include <iostream>

using namespace std;

/*
 * 리터럴이란?
 *
 * 값을 직접 나타내는 요소다.
 */

int main(int argc, char* argv[])
{
    // 정수 리터럴 10
    int x = 10;

    // 부동 소수점 리터럴 105.22
    double d = 105.22;

    // 문자열 리터럴 "abcd"
    const char* str = "abcd";

    // 부울 리터럴 true
    bool b = true;

    // 포인터 리터럴 nullptr
    void *p = nullptr;

    return 0;
}
